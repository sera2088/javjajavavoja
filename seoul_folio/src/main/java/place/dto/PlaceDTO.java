package place.dto;

public class PlaceDTO {
	private int placeNum;
	private String categori;
	private String placeName;
	private String placeAdress;
	private String latitude;
	private String longitude;
	private float rating;
	private String placePhone;
	private int placeScrab;
	private String placePhoto;
	private String placeLink;

	public PlaceDTO() {

	}

	public int getPlaceNum() {
		return placeNum;
	}

	public void setPlaceNum(int placeNum) {
		this.placeNum = placeNum;
	}

	public String getCategori() {
		return categori;
	}

	public void setCategori(String categori) {
		this.categori = categori;
	}

	public String getPlaceName() {
		return placeName;
	}

	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}

	public String getPlaceAdress() {
		return placeAdress;
	}

	public void setPlaceAdress(String placeAdress) {
		this.placeAdress = placeAdress;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public float getRating() {
		return rating;
	}

	public void setRating(float rating) {
		this.rating = rating;
	}

	public String getPlacePhone() {
		return placePhone;
	}

	public void setPlacePhone(String placePhone) {
		this.placePhone = placePhone;
	}

	public int getPlaceScrab() {
		return placeScrab;
	}

	public void setPlaceScrab(int placeScrab) {
		this.placeScrab = placeScrab;
	}

	public String getPlacePhoto() {
		return placePhoto;
	}

	public void setPlacePhoto(String placePhoto) {
		this.placePhoto = placePhoto;
	}

	public String getPlaceLink() {
		return placeLink;
	}

	public void setPlaceLink(String placeLink) {
		this.placeLink = placeLink;
	}
}
